def generate_character_frequency(text):
    """Generate character frequency analysis"""
    frequency = {}
    total_chars = len(text)
    
    for char in text:
        if char.isalnum() or char.isspace():
            frequency[char] = frequency.get(char, 0) + 1
    
    df = pd.DataFrame(list(frequency.items()), columns=['Character', 'Frequency'])
    df['Percentage'] = (df['Frequency'] / total_chars * 100).round(2)
    return df.sort_values(by='Frequency', ascending=False).head(20)

def generate_word_frequency(text):
    """Generate word frequency analysis"""
    words = text.lower().split()
    word_freq = {}
    stop_words = {'the', 'a', 'an', 'and', 'or', 'but', 'in', 'on', 'at', 'to', 'for', 'of', 'with', 'by'}
    
    for word in words:
        word = word.strip('.,!?;:()[]{}')
        if word and word not in stop_words and len(word) > 2:
            word_freq[word] = word_freq.get(word, 0) + 1
    
    df = pd.DataFrame(list(word_freq.items()), columns=['Word', 'Frequency'])
    return df.sort_values(by='Frequency', ascending=False).head(15)

def generate_document_stats(text):
    """Generate document statistics"""
    words = text.split()
    sentences = text.count('.') + text.count('!') + text.count('?')
    paragraphs = text.count('\n\n') + 1
    
    return {
        'total_characters': len(text),
        'total_words': len(words),
        'total_sentences': sentences,
        'total_paragraphs': paragraphs,
        'avg_word_length': round(len(text) / len(words), 2) if words else 0,
        'avg_sentence_length': round(len(words) / sentences, 2) if sentences else 0
    }

def process_file_async(file_data, filename, format_type, session_id):
    """Process uploaded PDF or image file asynchronously."""
    temp_path = None
    try:
        file_ext = os.path.splitext(filename)[1].lower()

        if file_ext == '.pdf':
            temp_path = os.path.join(app.config['UPLOAD_FOLDER'], f"{session_id}.pdf")
            with open(temp_path, 'wb') as f:
                f.write(file_data)

            with open(temp_path, 'rb') as f:
                images = pdf_to_images(io.BytesIO(f.read()))
        elif file_ext in {'.png', '.jpg', '.jpeg'}:
            images = [{
                'page': 1,
                'raw_data': image_to_png_bytes(file_data)
            }]
        else:
            raise ValueError("Unsupported file type. Please upload a PDF, PNG, JPG, or JPEG file.")
        
        # Process each page
        tasks = []
        worker_count = max(1, min(max_workers, len(images), 3))
        with ThreadPoolExecutor(max_workers=worker_count) as executor:
            for img in images:
                prompt = get_custom_prompt(format_type)
                tasks.append((img['page'], executor.submit(get_gemini_response, prompt, img['raw_data'])))
        
        # Combine results
        page_results = []
        for page_num, task in tasks:
            try:
                page_results.append((page_num, task.result(timeout=page_processing_timeout)))
            except FutureTimeoutError as e:
                raise TimeoutError(
                    f"Processing timed out on page {page_num}. Please try a smaller or clearer file."
                ) from e

        output_text = "\n\n--- Page Break ---\n\n".join(
            result for _, result in sorted(page_results, key=lambda item: item[0])
        )
        
        # Post-process based on format
        if format_type == "LaTeX":
            output_text = md_to_latex(output_text)
        
        # Save output
        output_ext = {
            "Plain Text": "txt",
            "Markdown": "md",
            "LaTeX": "tex"
        }.get(format_type, "txt")
        
        output_path = os.path.join(app.config['OUTPUT_FOLDER'], f"{session_id}.{output_ext}")
        with open(output_path, 'w', encoding='utf-8') as f:
            f.write(output_text)
        
        # Generate analytics
        char_freq = generate_character_frequency(output_text)
        word_freq = generate_word_frequency(output_text)
        doc_stats = generate_document_stats(output_text)
        
        return {
            'success': True,
            'output': output_text,
            'output_path': output_path,
            'analytics': {
                'character_frequency': char_freq.to_dict('records'),
                'word_frequency': word_freq.to_dict('records'),
                'document_stats': doc_stats,
                'total_pages': len(images)
            }
        }
    except Exception as e:
        return {'success': False, 'error': str(e)}
    finally:
        # Cleanup
        if temp_path and os.path.exists(temp_path):
            os.remove(temp_path)

