def translate_text_content(text, target_language):
    """Translate OCR output into the selected target language."""
    if model is None:
        raise RuntimeError("GOOGLE_API_KEY is missing. Add it to your .env file and restart the app.")

    if not text.strip():
        raise ValueError("No text provided for translation.")

    supported_languages = {
        'english': 'English',
        'hindi': 'Hindi',
        'tamil': 'Tamil',
        'telugu': 'Telugu',
        'malayalam': 'Malayalam'
    }

    normalized_language = target_language.strip().lower()
    language_name = supported_languages.get(normalized_language)
    if not language_name:
        raise ValueError("Unsupported language. Choose English, Hindi, Tamil, Telugu, or Malayalam.")

    translation_prompt = (
        f"Translate the following OCR output into {language_name}. "
        "Preserve the original structure, paragraphs, headings, bullet points, and formatting style. "
        "Return only the translated text without explanations.\n\n"
        f"OCR Output:\n{text}"
    )

    try:
        response = model.generate_content(translation_prompt)
        if not getattr(response, "text", None):
            raise RuntimeError("The AI model returned an empty translation.")
        return {
            'language_key': normalized_language,
            'language_name': language_name,
            'translated_text': response.text
        }
    except Exception as e:
        raise RuntimeError(f"Translation failed: {str(e)}") from e

def answer_document_question(document_text, question, language_label='Original OCR', chat_history=None):
    """Answer user questions using only the provided converted document."""
    if model is None:
        raise RuntimeError("GOOGLE_API_KEY is missing. Add it to your .env file and restart the app.")

    if not document_text.strip():
        raise ValueError("No converted document text is available for analysis.")

    if not question.strip():
        raise ValueError("Please enter a question for the document assistant.")

    history_lines = []
    for item in (chat_history or [])[-6:]:
        role = (item.get('role') or '').strip().lower()
        content = (item.get('content') or '').strip()
        if role in {'user', 'assistant'} and content:
            speaker = 'User' if role == 'user' else 'Assistant'
            history_lines.append(f"{speaker}: {content}")

    history_block = '\n'.join(history_lines) if history_lines else 'No previous conversation.'

    prompt = (
        "You are NeuralNotes Document Assistant.\n"
        "Answer the user's question using only the information found in the converted document text.\n"
        "If the document does not contain the answer, clearly say that the answer is not available in the document.\n"
        "Do not invent facts, page numbers, or references.\n"
        "Keep the response helpful, direct, and easy to understand.\n"
        "If the user asks for a summary, explanation, key points, definitions, or extracted facts, provide them only from the document.\n\n"
        f"Current document view: {language_label}\n\n"
        f"Conversation so far:\n{history_block}\n\n"
        f"Converted document text:\n{document_text}\n\n"
        f"User question:\n{question}"
    )

    try:
        response = model.generate_content(prompt)
        if not getattr(response, "text", None):
            raise RuntimeError("The AI model returned an empty answer.")
        return response.text.strip()
    except Exception as e:
        raise RuntimeError(f"Document chat failed: {str(e)}") from e

def send_document_email(
    recipient_email,
    subject,
    body,
    attachment_name,
    attachment_content,
    format_type='Plain Text',
    language_label='Original OCR',
    custom_message=''
):
    """Send the converted document as an email attachment."""
    smtp_host = os.getenv('SMTP_HOST')
    smtp_port = int(os.getenv('SMTP_PORT', '587'))
    smtp_user = os.getenv('SMTP_USER') or os.getenv('SMTP_USERNAME')
    smtp_password = os.getenv('SMTP_PASSWORD')
    from_email = os.getenv('FROM_EMAIL') or os.getenv('MAIL_FROM') or smtp_user
    smtp_use_tls = os.getenv('SMTP_USE_TLS', 'true').lower() != 'false'

    missing_settings = []
    if not smtp_host:
        missing_settings.append('SMTP_HOST')
    if not from_email:
        missing_settings.append('FROM_EMAIL or MAIL_FROM')

    if missing_settings:
        raise RuntimeError(
            "Email is not configured. Missing: "
            + ", ".join(missing_settings)
            + ". Supported variables: SMTP_HOST, SMTP_PORT, SMTP_USER or SMTP_USERNAME, SMTP_PASSWORD, FROM_EMAIL or MAIL_FROM."
        )

    message = EmailMessage()
    message['From'] = from_email
    message['To'] = recipient_email
    message['Subject'] = subject
    message.set_content(body)
    message.add_alternative(
        build_document_email_html(
            subject=subject,
            format_type=format_type,
            language_label=language_label,
            attachment_name=attachment_name,
            custom_message=custom_message,
            document_text=attachment_content
        ),
        subtype='html'
    )
    message.add_attachment(
        attachment_content.encode('utf-8'),
        maintype='text',
        subtype='plain',
        filename=attachment_name
    )

    context = ssl.create_default_context()
    with smtplib.SMTP(smtp_host, smtp_port, timeout=30) as server:
        if smtp_use_tls:
            server.starttls(context=context)
        if smtp_user and smtp_password:
            server.login(smtp_user, smtp_password)
        server.send_message(message)

