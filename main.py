@app.route('/api/upload', methods=['POST'])
@login_required
def upload_file():
    """Handle file upload and processing"""
    if 'file' not in request.files:
        return jsonify({'error': 'No file uploaded'}), 400
    
    file = request.files['file']
    format_type = request.form.get('format', 'Plain Text')
    
    if file.filename == '':
        return jsonify({'error': 'No file selected'}), 400
    
    allowed_extensions = {'.pdf', '.png', '.jpg', '.jpeg'}
    file_ext = os.path.splitext(file.filename)[1].lower()

    if file_ext not in allowed_extensions:
        return jsonify({'error': 'Only PDF, PNG, JPG, and JPEG files are allowed'}), 400
    
    try:
        # Generate unique session ID
        session_id = hashlib.md5(f"{datetime.now().timestamp()}{file.filename}".encode()).hexdigest()
        
        # Read file data
        file_data = file.read()
        
        # Process file
        result = process_file_async(file_data, file.filename, format_type, session_id)
        
        if result['success']:
            # Store in session
            session['last_output'] = result['output']
            session['last_format'] = format_type
            session['last_analytics'] = result['analytics']
            
            return jsonify({
                'success': True,
                'output': result['output'],
                'analytics': result['analytics'],
                'session_id': session_id
            })
        else:
            return jsonify({'error': result['error']}), 500
            
    except Exception as e:
        return jsonify({'error': str(e)}), 500

