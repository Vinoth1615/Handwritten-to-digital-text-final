def pdf_to_images(pdf_file):
    """Convert PDF pages to images"""
    doc = fitz.open(stream=pdf_file.read(), filetype="pdf")
    images = []
    for page_num in range(len(doc)):
        page = doc.load_page(page_num)
        pix = page.get_pixmap(matrix=fitz.Matrix(300/72, 300/72))  # Higher resolution
        img_data = pix.tobytes("png")
        images.append({
            'page': page_num + 1,
            'data': base64.b64encode(img_data).decode('utf-8'),
            'raw_data': img_data
        })
    return images

def image_to_png_bytes(image_data):
    """Normalize uploaded images to PNG bytes for model input."""
    with Image.open(io.BytesIO(image_data)) as img:
        if img.mode not in ("RGB", "RGBA"):
            img = img.convert("RGB")

        buffer = io.BytesIO()
        img.save(buffer, format="PNG")
        return buffer.getvalue()

def md_to_latex(md_content):
    """Convert Markdown to LaTeX"""
    latex_preamble = r"""\documentclass[a4paper]{article}
\usepackage[utf8]{inputenc}
\usepackage{geometry}
\usepackage{amsmath}
\usepackage{amssymb}
\usepackage{graphicx}
\usepackage{hyperref}
\geometry{margin=1in}
\title{Digitalized Notes}
\author{Generated by AI}
\date{\today}
\begin{document}
\maketitle
"""
    latex_body = pypandoc.convert_text(md_content, 'latex', format='md')
    return latex_preamble + latex_body + "\n\\end{document}"

